#include "libft.h"

char	*ft_strchr(char *s, const char c)
{
	int	a;

	a = 0;
	while (s[a] != c && s[a] != '\0')
		a++;
	if (s[a] == c && s[a] != '\0')
		return (&s[a]);
	return (NULL);
}
